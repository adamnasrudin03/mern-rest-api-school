const mongoose = require("mongoose");
const Schema = mongoose.Schema;

const Student = new Schema(
  {
    npm: {
      type: String,
      required: true,
    },
    name: {
      type: String,
      required: true,
    },
    gender: {
      type: String,
      required: true,
    },
    address: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

module.exports = mongoose.model("Student", Student);
